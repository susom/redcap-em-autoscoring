# Look up table to find the tvalues and tvalue percentage values
function lookup_score($tvalue_matrix, $raw_score) {

#	$this->module->emDebug("raw score = $raw_score and tvalue matrix = " . implode(',',$tvalue_matrix));
	if (isset($tvalue_matrix[$raw_score])) {
		$result = $tvalue_matrix[$raw_score];
   	} else {
		$result = NULL;
   	}
	return $result;	
}

# Look up the t-score and percentile rank based on raw score
